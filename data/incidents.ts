// Helper: get incident by ID
export function getIncidentById(id: string): Incident | undefined {
  return INCIDENTS.find((i) => i.id === id)
}

// Helper: get incidents available in a turn range
export function getIncidentsForTurn(turn: number): Incident[] {
  return INCIDENTS.filter((i) => i.minTurn <= turn && i.maxTurn >= turn)
}

// Alias used by turnEngine and gameStore
export function getIncidentsAvailableAtTurn(turn: number): Incident[] {
  return getIncidentsForTurn(turn)
}
